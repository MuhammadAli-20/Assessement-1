# Defining the correct password.
correct_pass = "12345"

attempt = 1

# Using a while loop to include 5 attempts

while attempt <= 5 :
    password = input("Please enter the correct password: ")
    if password == correct_pass:
        print("You are authorized") #Appropriate message
        break
    attempt = attempt+1
else:
     print("The authorities have been alerted")














        

    
    


   
    



    
    
      

    
    
    
    

    

   





    
    
    


    

