#  a for loop that counts up from 0 to 50 in increments of 1.
for x in range(0, 51):
     print(x)
     x+=1

#  a for loop that counts down from 50 to 0 in decrements of 1.
for s in range(50, -1, -1):
     print(s)

#  a for loop that counts up from 30 to 50 in increments of 1.
for e in range(30, 51):
     print(e)
     e = e+1

#  a for loop that counts down from 50 to 10 in decrements of 2.
for t in range(50, 9, -2):
     print(t)

# a for loop that counts up from 100 to 200 in increments of 5.
for r in range(100, 201, 5):
   print(r)
   
